package com.cboe.util;

/**
 * A base class for CBOE defined exceptions.
 * 
 * @author John Wickberg
 */
public class CboeException extends Exception
{
/**
 * CboeException constructor comment.
 */
public CboeException() {
	super();
}
/**
 * CboeException constructor comment.
 * @param s java.lang.String
 */
public CboeException(String s) {
	super(s);
}
}
