    public void testSkipCodecs () throws Exception {

        DataBufferBlock block = new DataBufferBlock(4096);
        block.setStorage(CurruptCodec, CurruptCodec.length);

        int countB = 0;
        ProdBuffer buffer = new ProdBuffer();
        while (block.read(buffer)) {
            buffer.rewind();

            countB++;

            int count = 0;

            while (buffer.nextCodecId() >= 0) {
                try {
                    buffer.skip();
                } catch (IOException e) {
                    assertEquals(
                            "ProdBuffer: skipping an invalid length of 0 at read position 1698 in buffer",
                            e.getMessage());
                    count = -1;
                    break;
                }
                count++;
            }
            assertTrue("expected some codecs ", count != 0);
        }
        assertEquals("expected number of buffers", 40, countB);

    }
